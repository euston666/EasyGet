    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // “保存”
        mainFrame.setEnabled(true);
        mainFrame.setVisible(true);//使主窗口显示在最前面
        if(jTextArea1.getText().equals("")){
            JOptionPane.showMessageDialog(this,"请输入URL！");
            this.setAlwaysOnTop(true);
            return;
        }
        if(!isURL(jTextArea1.getText())){
            JOptionPane.showMessageDialog(this,"URL格式错误！");
            this.setAlwaysOnTop(true);
            return;
        }
        String urlStr = jTextArea1.getText();
        String fileSavePath = jLabel4.getText();
        if(isfileExist(fileSavePath)){ //检测文件是否已存在
            JOptionPane.showMessageDialog(this,"文件已存在，请输入新的文件名！");
            this.setAlwaysOnTop(true);
            return;
        }
        this.dispose();
        mainFrame.setEnabled(true);
        TaskInfo downInfo = new TaskInfo(urlStr, fileSavePath, 3, mainFrame.getTaskID()); //默认启用3个线程
        new Downloader(downInfo, mainFrame).download();
        mainFrame.addTaskToVector(downInfo); //将任务添加到任务集合
    }//GEN-LAST:event_jButton2MouseClicked

