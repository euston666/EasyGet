    private boolean isURL(String tmp_url){
        try{
            URL url=new URL(tmp_url);
            return true;
        }
        catch(MalformedURLException e){
            return false;
        }	
    }
    
    private boolean isfileExist(String fileSavePath){
        File tmp_file = new File(fileSavePath);
        boolean exist = false;
        if(tmp_file.exists()){
            exist = true;
        }
        return exist;
    }
    
